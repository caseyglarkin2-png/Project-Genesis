export const getAdoptionColor = (status: PrimoFacility['adoptionStatus']): string => {
  switch (status) {
    case 'champion': return '#F59E0B'; // Amber - champion
    case 'full': return '#10B981'; // Green - full adoption
    case 'partial': return '#3B82F6'; // Blue - partial
    case 'pilot': return '#F97316'; // Orange - pilot
    case 'not_started': return '#64748B'; // Steel gray - not started
    default: return '#64748B';
  }
};

