/**
 * Get all unique states with facility counts
 * Useful for state filter dropdowns
 */
export const getStatesList = (): { state: string; count: number; adopted: number }[] => {
  const statesMap = new Map<string, { count: number; adopted: number }>();
  
  PRIMO_FACILITIES.forEach(f => {
    const current = statesMap.get(f.state) || { count: 0, adopted: 0 };
    current.count++;
    if (f.adoptionStatus !== 'not_started') current.adopted++;
    statesMap.set(f.state, current);
  });
  
  return Array.from(statesMap.entries())
    .map(([state, data]) => ({ state, ...data }))
    .sort((a, b) => a.state.localeCompare(b.state));
};

