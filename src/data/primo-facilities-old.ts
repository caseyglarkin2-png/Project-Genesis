/**
 * Search facilities by name, location, state, or ID
 * Optimized for facility switcher dropdown and search inputs
 */
export const searchFacilities = (query: string): PrimoFacility[] => {
  const searchTerm = query.toLowerCase().trim();
  if (!searchTerm) return PRIMO_FACILITIES;
  
  return PRIMO_FACILITIES.filter(f => 
    f.name.toLowerCase().includes(searchTerm) ||
    f.location.toLowerCase().includes(searchTerm) ||
    f.state.toLowerCase() === searchTerm ||
    f.id.toLowerCase().includes(searchTerm) ||
    f.brand.toLowerCase().includes(searchTerm)
  );
};

